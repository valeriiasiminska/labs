import time          # Модуль time дає функцію sleep() для паузи в секундах
import functools     # Модуль functools потрібен для wraps(), щоб не ламати метадані функції

def delay(seconds):
    """
    Декоратор-фабрика, який приймає кількість секунд затримки.
    Повертає сам декоратор.
    """
    def decorator(func):
        """
        Безпосередньо декоратор, який "обгортає" вихідну функцію.
        """
        @functools.wraps(func)
        def wrapper(*args, **kwargs):
            """
            Обгортка, яка спочатку викликає оригінальну функцію,
            а ПОТІМ робить штучну затримку перед поверненням результату.
            """
            # Спочатку обчислюємо результат вихідної функції
            result = func(*args, **kwargs)

            # Тепер додаємо штучну затримку ПЕРЕД поверненням результату
            time.sleep(seconds)

            # Повертаємо результат після паузи
            return result

        return wrapper

    return decorator
